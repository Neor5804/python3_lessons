#      Подсчет количества

#Напишем программу, которая считывает 10 чисел и определяет сколько из них больше 10.
counter = 0
for i in range(10):
    num = int(input())
    if num > 10:
        counter = counter + 1
print('Было введено', counter, 'чисел, больших 10.')


#Напишем программу, которая считывает 10 чисел и определяет сколько из них больше 10,
# посчитаем еще и количество нулей среди введенных чисел.
counter1 = 0
counter2 = 0
for i in range(10):
    num = int(input())
    if num > 10:
        counter1 = counter1 + 1
    if num == 0:
        counter2 = counter2 + 1
print('Было введено', counter1, 'чисел, больших 10.')
print('Было введено', counter2, 'нулей.' )

#Рассмотрим еще один пример: подсчитать количество чисел из диапазона [1;100],
# квадрат которых оканчивается на 4.
counter = 0
for i in range(1, 101):
    if i**2 % 10 == 4:
        counter = counter + 1
print(counter)



